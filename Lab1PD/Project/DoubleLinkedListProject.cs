namespace Lab1PD.Project
{
    /// <summary>
    /// DoubleLinkedListProject
    /// ================================
    /// 
    /// 1) Поля класса:
    ///    - head: ссылка на первый элемент списка
    ///    - tail: ссылка на последний элемент списка
    ///    - count: количество элементов
    /// 
    /// 2) Конструктор:
    ///    - при создании список пуст (head = null, tail = null, count = 0)
    /// 
    /// -------------------------------
    /// Методы (публичные):
    /// -------------------------------
    /// 
    /// End():
    /// - возвращает позицию "после последнего"
    /// - фактически это count + 1
    /// 
    /// Insert(x, p):
    /// - вставляет новый элемент со значением x на позицию p
    /// - проверяет корректность позиции (с помощью ValidatePosition)
    /// - если p = 1 → вставка в начало
    ///   * связываем новый узел с текущим head (через LinkNodes)
    ///   * head сдвигается на новый узел
    ///   * если список был пуст, tail указывает на тот же узел
    /// - если p = count + 1 → вставка в конец
    ///   * связываем tail с новым узлом (через LinkNodes)
    ///   * tail обновляется на новый узел
    /// - если p внутри списка → вставка в середину
    ///   * находим элемент, стоящий на позиции p (через GetNodeAt)
    ///   * связываем соседа слева с новым узлом и новый узел с текущим
    /// - увеличиваем count
    /// 
    /// Locate(x):
    /// - ищет элемент по значению
    /// - проходит список от головы к хвосту
    /// - если найден → возвращает его позицию
    /// - если нет → возвращает End()
    /// 
    /// Retrieve(p):
    /// - возвращает данные, хранящиеся на позиции p
    /// - вызывает GetNodeAt(p) и берёт его Data
    /// 
    /// Delete(p):
    /// - удаляет элемент на позиции p
    /// - находит узел через GetNodeAt
    /// - связывает предыдущий и следующий элементы (через LinkNodes)
    /// - если удаляем head → head сдвигается на следующий
    /// - если удаляем tail → tail сдвигается на предыдущий
    /// - уменьшаем count
    /// 
    /// Next(p):
    /// - возвращает позицию следующего элемента (p + 1)
    /// - если p последний → возвращает End()
    /// 
    /// Previous(p):
    /// - возвращает позицию предыдущего элемента (p - 1)
    /// - если p = 1 → ошибка (невозможно получить предыдущий)
    /// 
    /// Makenull():
    /// - очищает список
    /// - head = null, tail = null, count = 0
    /// - возвращает End()
    /// 
    /// First():
    /// - возвращает позицию первого элемента
    /// - если список пуст → возвращает End()
    /// 
    /// PrintList():
    /// - выводит элементы списка от головы до хвоста
    /// - проходит по Next и печатает Data каждого узла
    /// 
    /// -------------------------------
    /// Вспомогательные приватные методы:
    /// -------------------------------
    /// 
    /// GetNodeAt(position):
    /// - проверяет корректность позиции
    /// - проходит список от head до нужной позиции
    /// - возвращает ссылку на найденный узел
    /// 
    /// LinkNodes(left, right):
    /// - связывает два соседних узла
    /// - left.Next = right, right.Prev = left
    /// 
    /// ValidatePosition(position, allowEnd):
    /// - проверяет допустимость позиции
    /// - если allowEnd = true → допускается позиция "после хвоста" (для вставки)
    /// - иначе проверяется только 1..count
    /// 
    /// -------------------------------
    /// Вспомогательный класс DoubleLinkedListNode<T>:
    /// -------------------------------
    /// - хранит Data (значение элемента)
    /// - ссылку на следующий элемент (Next)
    /// - ссылку на предыдущий элемент (Prev)
    /// - используется как строительный блок двусвязного списка
    /// 
    /// </summary>
    public class DoubleLinkedListProject
    {
